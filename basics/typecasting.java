// typecasting
// implicit = byte -> short -> char -> int -> long -> float -> double
// explicit = double -> float -> long -> int -> char -> short -> byte

public class typecasting {
    public static void main(String[] args) {
        
        int a = 10;
        double b= 20.5;

        double x=a;
        int y=(int)b;

        System.out.println(x);
        System.out.println(y);
    }
}
